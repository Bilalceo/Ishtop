  skillProfiles.map((profile) => ({
    profile,
    normalizedKeywords: profile.keywords.map((keyword) => normalizeText(keyword)).filter(Boolean),
  }));

export function normalizeText(value: string): string {
  const lowercase = value.toLowerCase();
  const transliterated = Array.from(lowercase)
    .map((char) => CYRILLIC_TO_LATIN[char] ?? char)
    .join("")
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/['`]/g, "");

  return transliterated
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function uniqueSkills(skills: string[]): string[] {
  const seen = new Set<string>();
  const result: string[] = [];

  for (const skill of skills) {
    const clean = skill.trim();
    if (!clean) {
      continue;
    }

    const normalized = normalizeText(clean);
    if (!normalized || seen.has(normalized)) {
      continue;
    }

    seen.add(normalized);
    result.push(clean);
  }

  return result;
}

function scoreProfile(normalizedInput: string, normalizedKeywords: string[]): number {
  let score = 0;
  const inputTokens = normalizedInput.split(" ").filter(Boolean);

  for (const keyword of normalizedKeywords) {
    if (!keyword) {
      continue;
    }

    if (normalizedInput.includes(keyword)) {
      score += 3;
      continue;
    }

    const keywordTokens = keyword.split(" ").filter(Boolean);
    if (keywordTokens.length === 0) {
      continue;
    }

    const overlap = keywordTokens.filter((token) => token.length > 2 && inputTokens.includes(token)).length;
    if (overlap > 0) {
      score += overlap;
    }
  }

  return score;
}

